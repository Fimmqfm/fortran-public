program main
    implicit none

    real    ::  I
    read(*,*)   I

    if (I<=100000) then
        write(*,'(F9.2)') I*0.1
    else if (100000<I .and. I<=200000) then
        write(*,'(F9.2)') 100000*0.1+(I-100000)*0.075
    else if (200000<I .and. I<=400000) then
        write(*,'(F9.2)') 100000*0.1+100000*0.075+(I-200000)*0.05
    else if (400000<I .and. I<=600000) then
        write(*,'(F9.2)') 100000*0.1+100000*0.075+200000*0.05+(I-400000)*0.03
    else if (600000<I .and. I<=1000000) then
        write(*,'(F9.2)') 100000*0.1+100000*0.075+200000*0.05+200000*0.03+(I-600000)*0.015
    else if (I>1000000) then
        write(*,'(F0.2)') 100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(I-1000000)*0.01
    end if

    select case(int(I))
        case(0:100000)
            write(*,'(F9.2)') I*0.1
        case(100001:200000)
            write(*,'(F9.2)') 100000*0.1+(I-100000)*0.075
        case(200001:400000)
            write(*,'(F9.2)') 100000*0.1+100000*0.075+(I-200000)*0.05
        case(400001:600000)
            write(*,'(F9.2)') 100000*0.1+100000*0.075+200000*0.05+(I-400000)*0.03
        case(600001:1000000)
            write(*,'(F9.2)') 100000*0.1+100000*0.075+200000*0.05+200000*0.03+(I-600000)*0.015
        case(1000001:)
            write(*,'(F0.2)') 100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(I-1000000)*0.01
    end select

end
